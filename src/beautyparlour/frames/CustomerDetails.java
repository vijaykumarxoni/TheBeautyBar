    private void jButtonUpdateMoneyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateMoneyActionPerformed
        // TODO add your handling code here:
        int dialogButton = 0;
        if (this.jRadioWithDraw.isSelected() || this.jRadioButtonDeposite.isSelected()) {
            if (!this.jTextFieldUpdateMoney.getText().equalsIgnoreCase("")) {
                try {
                    double debt = Double.parseDouble("" + this.jLabelCustomerDebt.getText());
                    double addMoney = Double.parseDouble(this.jTextFieldUpdateMoney.getText());
                    double newDebt = 0.0;
                    if (this.jRadioButtonDeposite.isSelected()) {
                        newDebt = debt + addMoney;
                        dialogButton = JOptionPane.showConfirmDialog(null, "Are you sure you wanna desposite " + addMoney + " to " + this.jLabelCustomerName.getText() + " account. ", "Update record", JOptionPane.YES_NO_OPTION);

                    }
                    if (this.jRadioWithDraw.isSelected()) {
                        newDebt = debt - addMoney;
                        dialogButton = JOptionPane.showConfirmDialog(null, "Are you sure you wanna withdraw " + addMoney + " from " + this.jLabelCustomerName.getText() + " account. ", "Update record", JOptionPane.YES_NO_OPTION);

                    }

                    if (dialogButton == JOptionPane.YES_OPTION) {

                        int i = customerDao.modifyCustomerDebt(cust_id, newDebt);

                        if (i > 0) {
                            JOptionPane.showMessageDialog(rootPane, "Updated Successfully!");
                            this.jButtonUpdate.setVisible(true);
                            this.jButtonUpdateMoney.setVisible(false);
                            this.jTextFieldUpdateMoney.setVisible(false);
                            this.jLabelAddMoney.setVisible(false);
                        }
                    }
                    showCustomerDebt(cust_id);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(rootPane, "Amount must be numeric!");

                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Enter amount first!");

            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Select money Deposite or Withdraw option first !");

        }
    }//GEN-LAST:event_jButtonUpdateMoneyActionPerformed

