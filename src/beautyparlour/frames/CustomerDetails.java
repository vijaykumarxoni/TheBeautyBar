    public void showInCustomerSalesDetailTable(int customer_id, String date, int saleId) {
        try {
            tableModelCustomerSaleDetail.setRowCount(0);
            int counter = 1;
            ServiceDao ser = new ServiceDaoImpl();
            sale_details_list = saleDeatail.getAllSaleDetailbyCustomerId(customer_id, date, saleId);
            for (SaleDetailBeans salesDetails : sale_details_list) {
                Vector V = new Vector();
                V.add(ser.getServiceById(salesDetails.getService_id()).getService_name());
                V.add(salesDetails.getPrice());

                tableModelCustomerSaleDetail.addRow(V);
            }
        } catch (Exception e) {
            System.out.println("No data");
        }
    }

    public void showInCustomerProductSalesDetailTable(int saleId) {
        try {
            tableModelProductCustomerSaleDetail.setRowCount(0);
            productSaleDetails = productSaleDeatailDao.getAllProductSaleDetailsBySaleId(saleId);
            for (ProductSaleDetailBean psdb : productSaleDetails) {
                Vector V = new Vector();
                V.add(psdb.getInventoryBean().getInventoryName());
                V.add(psdb.getTotalCost() / psdb.getQuantity());
                V.add(psdb.getQuantity());
                V.add(psdb.getTotalCost());

                tableModelProductCustomerSaleDetail.addRow(V);
            }
        } catch (Exception e) {
            System.out.println("No data");
        }
    }

    public void showInSaleTable(int customer_id) {
        tableModelCustomerSale.setRowCount(0);
        sale_list = sale.getAllSalesByCustomId(customer_id);
        for (SaleBeans salesDetails : sale_list) {
            Vector V = new Vector();
            V.add(salesDetails.getSale_id());
            V.add(salesDetails.getSale_date());
            V.add(salesDetails.getDiscount() + " " + salesDetails.getDiscount());

            V.add(salesDetails.getTotal_bill());
            V.add(salesDetails.getPaid());
            V.add(salesDetails.getTotal_bill() - salesDetails.getPaid());

            tableModelCustomerSale.addRow(V);
        }
    }

    public void showInProductSaleTable(int customer_id) {
        tableModelProductCustomerSale.setRowCount(0);
        productSalesList = productSaleDao.getAllSalesByCustomId(customer_id);
        for (ProductSaleBean psb : productSalesList) {
            Vector V = new Vector();
            V.add(psb.getProdSaleId());
            V.add(psb.getCreatedAt());
            V.add(psb.getDiscount() + " " + psb.getDiscount());
            V.add(psb.getTotalCost());
            V.add(psb.getPaid());
            V.add(psb.getTotalCost() - psb.getPaid());

            tableModelProductCustomerSale.addRow(V);
        }
    }

