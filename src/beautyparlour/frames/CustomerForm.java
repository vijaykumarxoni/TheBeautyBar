    public void showInTable() {
        tableModelCustomer.setRowCount(0);

        int serial = 0;
        customer_list = customerDao.getAllCustomer();
        for (CustomerBean customerBeans : customer_list) {
            Vector V = new Vector();
            serial++;

            V.add(serial);
            V.add(customerBeans.getCustomer_id());
            V.add(customerBeans.getCustomer_name());
            V.add(customerBeans.getCustomer_contact());
            V.add(customerBeans.getCustomer_address());
            V.add(customerBeans.getCustomer_inserted_date());

            tableModelCustomer.addRow(V);
        }
    }

