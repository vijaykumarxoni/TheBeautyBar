    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        //int customer_id=customer_list.get(this.jTableCustomer.getSelectedRow()).getCustomer_id();
        int customer_id = (int) this.jTableCustomer.getValueAt(this.jTableCustomer.getSelectedRow(), 1);

        String customer_name = this.jTextFieldCustomerName.getText();
        String customer_contact = this.jTextFieldCustomerContact.getText();
        String customer_address = this.jTextFieldCustomerAddress.getText();

        String customer_updated_date = CurrentDate.getCurrentDate();

        if (customer_name.equals("")) {

            new playAudio().playErrorSound();
            JOptionPane.showMessageDialog(this, "Insert Customer Name ");

        } else {

            customerDao.updateCustomer(new CustomerBean(customer_id, customer_name, customer_contact, customer_address, customer_updated_date, 1, 0.0));

            showInTable();
            new playAudio().playSuccessSound();
            jButtonUpdate.setEnabled(false);
            jButtonSave.setEnabled(true);
            jButtonClear1.setEnabled(true);

            ClearFields();

            jButtonDelete.setEnabled(false);
        }
    }//GEN-LAST:event_jButtonUpdateActionPerformed

